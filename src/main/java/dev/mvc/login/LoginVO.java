package dev.mvc.login;

public class LoginVO {

  private int login_no = 0;
  private String login_time = null;
  private int consumer_no = 0;
  private String login_ip = null;
  
  public int getLogin_no() {
    return login_no;
  }
  public void setLogin_no(int login_no) {
    this.login_no = login_no;
  }
  public String getLogin_time() {
    return login_time;
  }
  public void setLogin_time(String login_time) {
    this.login_time = login_time;
  }
  public int getConsumer_no() {
    return consumer_no;
  }
  public void setConsumer_no(int consumer_no) {
    this.consumer_no = consumer_no;
  }
  public String getLogin_ip() {
    return login_ip;
  }
  public void setLogin_ip(String login_ip) {
    this.login_ip = login_ip;
  }
  
  

  

}
